class TestListSortedOutput:
    """list output should be alphabetically sorted by domain."""

    def test_output_is_sorted_alphabetically(self, tmp_path, monkeypatch):
        dirs = _patch_env(monkeypatch, tmp_path)
        for name in ["zebra.test", "alpha.test", "middle.test"]:
            dirs["nginx_avail"].joinpath(f"{name}.conf").write_text(
                f"server {{ listen 80; server_name {name}; root /x; }}"
            )

        result = runner.invoke(app, ["list", "--provider", "nginx"], catch_exceptions=False)
        assert result.exit_code == 0
        lines = result.stdout.splitlines()
        domain_lines = [l for l in lines if any(n in l for n in ["alpha.test", "middle.test", "zebra.test"])]
        # Extract the domain names in order they appear
        ordered = [l for l in domain_lines if "alpha.test" in l or "middle.test" in l or "zebra.test" in l]
        domains_in_order = []
        for line in ordered:
            if "alpha.test" in line:
                domains_in_order.append("alpha.test")
            elif "middle.test" in line:
                domains_in_order.append("middle.test")
            elif "zebra.test" in line:
                domains_in_order.append("zebra.test")
        # Deduplicate while preserving order (domain appears in domain col and config path col)
        seen = []
        for d in domains_in_order:
            if d not in seen:
                seen.append(d)
        assert seen == sorted(seen), f"Expected sorted output, got {seen}"


