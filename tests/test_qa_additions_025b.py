class TestOSErrorDuringIterdir:
    """OSError during iterdir() must be handled gracefully — no crash, empty result."""

    def test_oserror_on_iterdir_skipped(self, tmp_path, monkeypatch):
        """Simulate OSError when iterating a directory."""
        nginx_avail = tmp_path / "nginx" / "sites-available"
        nginx_enabled = tmp_path / "nginx" / "sites-enabled"
        nginx_avail.mkdir(parents=True)
        nginx_enabled.mkdir(parents=True)

        monkeypatch.setattr("vhost_helper.main.NGINX_SITES_AVAILABLE", nginx_avail)
        monkeypatch.setattr("vhost_helper.main.NGINX_SITES_ENABLED", nginx_enabled)
        monkeypatch.setattr("vhost_helper.main.NGINX_SITES_DISABLED", None)
        monkeypatch.setattr("vhost_helper.main.APACHE_SITES_AVAILABLE", tmp_path / "nope")
        monkeypatch.setattr("vhost_helper.main.APACHE_SITES_ENABLED", tmp_path / "nope2")
        monkeypatch.setattr("vhost_helper.main.APACHE_SITES_DISABLED", None)

        original_iterdir = Path.iterdir

        def patched_iterdir(self):
            if str(self) == str(nginx_avail):
                raise OSError("Simulated OSError on iterdir")
            return original_iterdir(self)

        monkeypatch.setattr(Path, "iterdir", patched_iterdir)

        # Should not raise — OSError is caught internally
        results = _scan_all_vhosts_locally(filter_provider=ServerType.NGINX)
        assert isinstance(results, list)

    def test_oserror_on_enabled_iterdir_falls_back_gracefully(self, tmp_path, monkeypatch):
        """OSError on sites-enabled iterdir means all found are shown as Disabled."""
        nginx_avail = tmp_path / "nginx" / "sites-available"
        nginx_enabled = tmp_path / "nginx" / "sites-enabled"
        nginx_avail.mkdir(parents=True)
        nginx_enabled.mkdir(parents=True)
        nginx_avail.joinpath("ext.test.conf").write_text(
            "server { listen 80; server_name ext.test; root /x; }"
        )

        monkeypatch.setattr("vhost_helper.main.NGINX_SITES_AVAILABLE", nginx_avail)
        monkeypatch.setattr("vhost_helper.main.NGINX_SITES_ENABLED", nginx_enabled)
        monkeypatch.setattr("vhost_helper.main.NGINX_SITES_DISABLED", None)
        monkeypatch.setattr("vhost_helper.main.APACHE_SITES_AVAILABLE", tmp_path / "nope")
        monkeypatch.setattr("vhost_helper.main.APACHE_SITES_ENABLED", tmp_path / "nope2")
        monkeypatch.setattr("vhost_helper.main.APACHE_SITES_DISABLED", None)

        original_iterdir = Path.iterdir

        def patched_iterdir(self):
            if str(self) == str(nginx_enabled):
                raise OSError("Permission denied on sites-enabled")
            return original_iterdir(self)

        monkeypatch.setattr(Path, "iterdir", patched_iterdir)

        results = _scan_all_vhosts_locally(filter_provider=ServerType.NGINX)
        # ext.test should still be found, just shown as Disabled
        assert isinstance(results, list)
        if results:
            ext = next((v for v in results if v.domain == "ext.test"), None)
            if ext:
                assert ext.status == "Disabled"


class TestGeneralExceptionReadingConfig:
    """Non-PermissionError exceptions while reading a config file are handled."""

    def test_generic_ioerror_reading_config_skipped(self, tmp_path, monkeypatch):
        nginx_avail = tmp_path / "nginx" / "sites-available"
        nginx_enabled = tmp_path / "nginx" / "sites-enabled"
        nginx_avail.mkdir(parents=True)
        nginx_enabled.mkdir(parents=True)

        # Write one valid and one that will raise on read_text
        valid_conf = nginx_avail.joinpath("valid.test.conf")
        valid_conf.write_text("server { listen 80; server_name valid.test; root /x; }")
        broken_conf = nginx_avail.joinpath("broken.test.conf")
        broken_conf.write_text("server { listen 80; server_name broken.test; root /x; }")

        monkeypatch.setattr("vhost_helper.main.NGINX_SITES_AVAILABLE", nginx_avail)
        monkeypatch.setattr("vhost_helper.main.NGINX_SITES_ENABLED", nginx_enabled)
        monkeypatch.setattr("vhost_helper.main.NGINX_SITES_DISABLED", None)
        monkeypatch.setattr("vhost_helper.main.APACHE_SITES_AVAILABLE", tmp_path / "nope")
        monkeypatch.setattr("vhost_helper.main.APACHE_SITES_ENABLED", tmp_path / "nope2")
        monkeypatch.setattr("vhost_helper.main.APACHE_SITES_DISABLED", None)

        original_read_text = Path.read_text

        def patched_read_text(self, *args, **kwargs):
            if self.name == "broken.test.conf":
                raise IOError("Simulated IOError reading config")
            return original_read_text(self, *args, **kwargs)

        monkeypatch.setattr(Path, "read_text", patched_read_text)

        # Should not raise; broken config is skipped, valid config still found
        results = _scan_all_vhosts_locally(filter_provider=ServerType.NGINX)
        assert isinstance(results, list)
        domains = [v.domain for v in results]
        assert "valid.test" in domains
        assert "broken.test" not in domains


